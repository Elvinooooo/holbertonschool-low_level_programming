#include "3-calc.h"
/**
*op_add - function that adds two integers
*@a: first integer
*@b: second integer
*Return: the result of addition
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
*op_sub - function that subtracts two integers
*@a: first integer
*@b: second integer
*Return: the result of subtraction
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
*op_mul - function that multiplicates two integers
*@a: first integer
*@b: second integer
*Return: the result of multiplication
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
*op_div - function that divides two integers
*@a: first integer
*@b: second integer
*Return: the result of division
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
*op_mod - function that returns the remainder of the division
*@a: first integer
*@b: second integer
*Return: the remainder
*/

int op_mod(int a, int b)
{
	return (a % b);
}
